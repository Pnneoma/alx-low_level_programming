/**
 * reverse_array - a function that reverses the content of an array of integers
 * @x: array
 * @z: numbers of elements of array
 *
 * Return: void
 */
void reverse_array(int *x, int z)
{
	int a;
	int b;

	for (a = 0; a < b--; a++)
	{
		b = x[a];
		x[a] = x[z];
		x[z] = b;
	}
}
